public class Ejercicio14 {
    public static void main(String[] args) {
        // Utilizar un bucle for para recorrer los números del 1 al 100
        for (int i = 1; i <= 100; i++) {
            // Verificar si el número es múltiplo de 3
            if (i % 3 == 0) {
                // Imprimir el número si es múltiplo de 3
                System.out.println(i);
            }
        }
    }
}


    
